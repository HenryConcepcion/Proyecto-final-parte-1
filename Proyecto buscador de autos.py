def mostrar_menu():
  print("\nMenú de Búsqueda de Autos:")
  print("1. Buscar por Tipo de Auto")
  print("2. Buscar por Nombre")
  print("3. Finalizar Búsqueda")
  opcion = input("Ingresa tu opción: ")
  return opcion

def buscar_por_tipo():
              
              car_types = set()
              for _, info_auto in cars.items():
                car_types.add(info_auto["tipo"])

              
              print("Tipos de autos disponibles:")
              for i, tipo in enumerate(car_types):
                print(f"{i+1}. {tipo}")

              
              while True:
                try:
                  opcion_tipo = int(input("Ingresa el número del tipo de auto que estás buscando: "))
                  if 1 <= opcion_tipo <= len(car_types):
                    break
                  else:
                    print("Opción inválida. Por favor, ingresa un número válido.")
                except ValueError:
                  print("Ingresa un número válido.")

              
              tipo_auto = list(car_types)[opcion_tipo - 1]

              autos_encontrados = False
              for nombre_auto, info_auto in cars.items():
                if tipo_auto.lower() in info_auto["tipo"].lower():
                  print(f"\n{nombre_auto}:")
                  print(f"  Tipo: {info_auto['tipo']}")
                  print(f"  Descripción: {info_auto['descripcion']}")
                  print(f"  Año: {info_auto['year']}") 
                  print(f"  Kilometraje: {info_auto['kilometraje']}")
                  print(f"  Precio: ${info_auto['precio']}")
                  autos_encontrados = True

              if not autos_encontrados:
                print(f"No se encontraron autos que coincidan con '{tipo_auto}'.")

def buscar_por_nombre():
        nombre_auto = input("Ingresa el nombre del auto que estás buscando: ")

        
        if nombre_auto.lower() in cars:
          print(f"\n{nombre_auto}:")
          print(f"  Tipo: {cars[nombre_auto]['tipo']}")
          print(f"  Descripción: {cars[nombre_auto]['descripcion']}")
          print(f"  Año: {cars[nombre_auto]['year']}") 
          print(f"  Kilometraje: {cars[nombre_auto]['kilometraje']}")
          print(f"  Precio: ${cars[nombre_auto]['precio']}")
          return  

       
        autos_encontrados = False
        for nombre_car, info_car in cars.items():
          if nombre_auto.lower() in nombre_car.lower():
            print(f"\n{nombre_car}:")
            print(f"  Tipo: {info_car['tipo']}")
            print(f"  Descripción: {info_car['descripcion']}")
            print(f"  Año: {info_car['year']}")
            print(f"  Kilometraje: {info_car['kilometraje']}")
            print(f"  Precio: ${info_car['precio']}")
            autos_encontrados = True

        if not autos_encontrados:
          print(f"No se encontró ningún auto que coincida con '{nombre_auto}'.")

while True:
  opcion = mostrar_menu()

